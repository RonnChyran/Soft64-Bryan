using System;
using System.IO;

namespace Soft64.MipsR4300
{
    public sealed class Cache : Stream
    {
        private Int64 m_Position;
        private Stream m_SourceStream;
        private Int32 m_CacheMinSize;
        private Int32 m_CacheMaxSize;

        public Cache(Int32 minSize, Int32 maxSize, Stream sourceStream)
        {
            m_CacheMinSize = minSize;
            m_CacheMaxSize = maxSize;
            m_SourceStream = sourceStream;
            m_Position = 0;
        }

        public override bool CanRead
        {
            get { throw new NotImplementedException(); }
        }

        public override bool CanSeek
        {
            get { throw new NotImplementedException(); }
        }

        public override bool CanWrite
        {
            get { throw new NotImplementedException(); }
        }

        public override void Flush()
        {
            throw new NotImplementedException();
        }

        public override long Length
        {
            get { throw new NotImplementedException(); }
        }

        public override long Position
        {
            get
            {
                throw new NotImplementedException();
            }
            set
            {
                throw new NotImplementedException();
            }
        }

        public override int Read(byte[] buffer, int offset, int count)
        {
            throw new NotImplementedException();
        }

        public override long Seek(long offset, SeekOrigin origin)
        {
            throw new NotImplementedException();
        }

        public override void SetLength(long value)
        {
            throw new NotImplementedException();
        }

        public override void Write(byte[] buffer, int offset, int count)
        {
            throw new NotImplementedException();
        }
    }
}